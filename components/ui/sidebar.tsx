const SIDEBAR_COOKIE_NAME = "sidebar:state"
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
const SIDEBAR_WIDTH = "16rem"
const SIDEBAR_WIDTH_MOBILE = "18rem"
const SIDEBAR_WIDTH_ICON = "3rem"
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

type SidebarContext = {
  state: "expanded" | "collapsed"
  open: boolean
  setOpen: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
  isPinned: boolean
}

const SidebarContext = React.createContext<SidebarContext | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }

  return context
}

const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    defaultOpen?: boolean
    open?: boolean
    onOpenChange?: (open: boolean) => void
  }
>(
  (
    {
      defaultOpen = false, // Default to not pinned for hover behavior
      open: pinnedProp, // 'open' prop now controls 'isPinned'
      onOpenChange: setPinnedProp, // 'onOpenChange' receives 'isPinned' state
      className,
      style,
      children,
      ...props
    },
    ref
  ) => {
    const isMobile = useIsMobile()
    const [openMobile, setOpenMobile] = React.useState(false)
    const [isHoverNear, setIsHoverNear] = React.useState(false)

    const getInitialPinnedState = () => {
      if (typeof document !== 'undefined') {
        const cookieValue = document.cookie
          .split('; ')
          .find(row => row.startsWith(`${SIDEBAR_COOKIE_NAME}=`))
          ?.split('=')[1];
        if (cookieValue) {
          return cookieValue === 'true';
        }
      }
      return defaultOpen;
    }

    const [_isPinned, _setIsPinned] = React.useState(getInitialPinnedState)
    const isPinned = pinnedProp ?? _isPinned

    const setIsPinned = React.useCallback(
      (value: boolean | ((value: boolean) => boolean)) => {
        const newPinnedState = typeof value === 'function' ? value(isPinned) : value
        if (setPinnedProp) {
          setPinnedProp(newPinnedState)
        } else {
          _setIsPinned(newPinnedState)
        }

        if (typeof document !== 'undefined') {
          document.cookie = `${SIDEBAR_COOKIE_NAME}=${newPinnedState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`
        }
      },
      [setPinnedProp, isPinned]
    )

    // toggleSidebar now toggles 'isPinned' on desktop
    const toggleSidebar = React.useCallback(() => {
      return isMobile
        ? setOpenMobile((currentOpenMobile) => !currentOpenMobile)
        : setIsPinned((currentIsPinned) => !currentIsPinned)
    }, [isMobile, setIsPinned, setOpenMobile])

    // Adds a keyboard shortcut to toggle the sidebar (pinning on desktop).
    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault()
          toggleSidebar()
        }
      }

      if (typeof window !== 'undefined') {
        window.addEventListener("keydown", handleKeyDown)
        return () => window.removeEventListener("keydown", handleKeyDown)
      }
    }, [toggleSidebar])

    const actualOpen = isMobile ? openMobile : (isHoverNear || isPinned)
    const state = actualOpen ? "expanded" : "collapsed"

    const contextValue = React.useMemo<SidebarContext>(
      () => ({
        state,
        open: actualOpen,
        setOpen: isMobile ? setOpenMobile : setIsPinned,
        isMobile,
        openMobile,
        setOpenMobile,
        toggleSidebar,
        isPinned, // Expose isPinned state
      }),
      [state, actualOpen, setIsPinned, isMobile, openMobile, setOpenMobile, toggleSidebar, isPinned]
    )

    return (
      <SidebarContext.Provider value={contextValue}>
        <TooltipProvider delayDuration={0}>
          <div
            onMouseEnter={() => {
              if (!isMobile) {
                setIsHoverNear(true);
              }
            }}
            onMouseLeave={() => {
              if (!isMobile) {
                setIsHoverNear(false);
              }
            }}
            style={
              {
                "--sidebar-width": SIDEBAR_WIDTH,
                "--sidebar-width-icon": SIDEBAR_WIDTH_ICON,
                ...style,
              } as React.CSSProperties
            }
            className={cn(
              "group/sidebar-wrapper flex min-h-0 w-full outline-none",
              className
            )}
            tabIndex={(!isMobile && !isHoverNear && !isPinned) ? -1 : undefined}
            ref={ref}
            {...props}
          >
            {children}
          </div>
        </TooltipProvider>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"

const Sidebar = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    side?: "left" | "right"
    variant?: "sidebar" | "floating" | "inset"
    collapsible?: "offcanvas" | "icon" | "none"
  }
>(
  (
    {
      side = "left",
      variant = "sidebar",
      collapsible = "offcanvas",
      className,
      children,
      ...props
    },
    ref
  ) => {
    const { isMobile, state, openMobile, setOpenMobile } = useSidebar()

    if (collapsible === "none") {
      return (
        <div
          className={cn(
            className
          )}
          ref={ref}
          {...props}
        >
          {children}
        </div>
      )
    }

    if (isMobile) {
      return (
        <Sheet open={openMobile} onOpenChange={setOpenMobile} {...props}>
          <SheetContent
            data-sidebar="sidebar"
            data-mobile="true"
            className="w-[--sidebar-width] bg-sidebar p-0 text-sidebar-foreground [&>button]:hidden"
            style={
              {
                "--sidebar-width": SIDEBAR_WIDTH_MOBILE,
              } as React.CSSProperties
            }
            side={side}
          >
          </SheetContent>
        </Sheet>
      )
    }

    return (
      <div
    ref={ref}
    className={cn(
      "group peer text-sidebar-foreground",
      // Apply fixed positioning for non-mobile, non-floating sidebars
      !isMobile && variant !== "floating" && "fixed top-0 bottom-0 z-30 h-full",
      !isMobile && variant !== "floating" && side === "left" && "left-0",
      !isMobile && variant !== "floating" && side === "right" && "right-0",
      // For floating or mobile, maintain relative positioning or let Sheet handle it
      (isMobile || variant === "floating") && "relative",
      // Original logic for collapsing offcanvas - this might need adjustment if outer is fixed
      // but w-0 on inner div should effectively hide it.
      // Keeping this on the outer fixed element might be fine if inner is w-0.
      state === "collapsed" && collapsible === "offcanvas" && !isMobile && variant !== "floating" && "pointer-events-none", // Outer fixed container can stay, but not interact
      className // User-provided classNames for the outer container
    )}
    tabIndex={state === "collapsed" && collapsible === "offcanvas" && !isMobile && variant !== "floating" ? -1 : undefined}
        data-state={state}
        data-collapsible={state === "collapsed" ? collapsible : ""}
        data-variant={variant}
        data-side={side}
      >
        {/* Sidebar content without fixed positioning */}
        <div
          className={cn(
            "flex flex-col", // Ensure inner div takes full height for fixed layout
            {
              // State-based visibility and dimensions
              "opacity-100": state === "expanded" || (state === "collapsed" && collapsible === "icon"),
              "opacity-0": state === "collapsed" && collapsible === "offcanvas" && !isMobile, // Opacity for non-mobile offcanvas

              "w-[--sidebar-width]": state === "expanded",
              "w-0": state === "collapsed" && collapsible === "offcanvas",
              "w-[--sidebar-width-icon]": state === "collapsed" && collapsible === "icon",

              "overflow-hidden": state === "collapsed" && collapsible === "offcanvas",

              // When fully hidden (collapsed + offcanvas)
              // Apply invisible and pointer-events-none to the inner div to ensure it's fully hidden
              "invisible pointer-events-none": state === "collapsed" && collapsible === "offcanvas",
              
              // Border logic: apply to the correct side
              "border-sidebar-border": variant !== "floating" && (state === "expanded" || (state === "collapsed" && collapsible === "icon")),
              "border-r": side === "left" && variant !== "floating" && (state === "expanded" || (state === "collapsed" && collapsible === "icon")),
              "border-l": side === "right" && variant !== "floating" && (state === "expanded" || (state === "collapsed" && collapsible === "icon")),
              
              // Remove border if offcanvas collapsed (as it's w-0 and invisible)
              "border-none shadow-none outline-none": state === "collapsed" && collapsible === "offcanvas",


              // Floating variant specific styles (margin, rounding, and border/shadow if not offcanvas-collapsed)
              "m-2 rounded-lg": variant === "floating", // These apply if variant is floating
              "border shadow": variant === "floating" && (state === "expanded" || (state === "collapsed" && collapsible === "icon")),
            },
          )}
          // Spread other props to the inner div as per original structure
          // However, ref is on the outer div. Let's ensure props like style, id also go to the outer div if that was intended.
          // The original code spreads {...props} on this inner div. We'll keep that.
          {...props}
        >
          <div
            data-sidebar="sidebar"
            className="h-full flex flex-col" // Ensure this content wrapper is also full height and flex-col
          >
            {children}
          </div>
        </div>
      </div>
    )
  }
)
Sidebar.displayName = "Sidebar"

